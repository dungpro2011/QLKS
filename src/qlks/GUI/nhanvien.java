    private void tbQLNVMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbQLNVMousePressed
        //vi tri click chuot vao table
        row = tbQLNV.getSelectedRow();

        nv = nvDTO.getNhanVien(row);
        tfMNV.setText(model.getValueAt(row, 0).toString());
        tfH.setText(model.getValueAt(row, 1).toString());
        tfT.setText(model.getValueAt(row, 2).toString());
        tfNS.setText(model.getValueAt(row, 3).toString());
        if(model.getValueAt(row, 4).toString().compareTo("Nam")==0){
            rdNam.setSelected(true);
        }else{
            rdNu.setSelected(true);
        }
        tfDC.setText(model.getValueAt(row, 5).toString());
        tfCMND.setText(model.getValueAt(row, 6).toString());
        tfSDT.setText(model.getValueAt(row, 7).toString());
        tfNVL.setText(model.getValueAt(row, 8).toString());
        tfL.setText(String.valueOf(model.getValueAt(row, 9).toString()));
    }//GEN-LAST:event_tbQLNVMousePressed

    private void btLocActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btLocActionPerformed
        int luong = 0;
        try{
            luong = Integer.parseInt(tfL.getText());
        }catch(Exception e){
            luong = -1;
        }
        
        nv = new NhanVien();
        nv.setMaNhanVien(tfMNV.getText());
        nv.setHo(tfH.getText());
        nv.setTen(tfT.getText());
        nv.setNgaySinh(tfNS.getText());
        if(rdNam.isSelected()){
            nv.setGioiTinh("Nam");
        }else{
            if(rdNu.isSelected()){
                nv.setGioiTinh("Nữ");
            } 
        }
        nv.setDiaChi(tfDC.getText());
        nv.setCMMD(tfCMND.getText());
        nv.setDienThoai(tfSDT.getText());
        nv.setNgayVaoLam(tfNVL.getText());
        nv.setLuongThang(luong);
        ArrayList<NhanVien> dstnv = null;
        if(cbNV.getSelectedIndex()==0){
            dstnv = nvDTO.timNhanVienChichXac(nv);
        }else{
            if(cbNV.getSelectedIndex()==1){
                dstnv = nvDTO.timNhanVienTheoMuc(nv);
            }
        }
        
        if(dstnv.size()>0){
            tbQLNV.setModel(new ModelNhanVien(dstnv).getModel());
        }else{
            JOptionPane.showMessageDialog(null, "Không tìm thấy nhân viên");
        }                
    }//GEN-LAST:event_btLocActionPerformed

