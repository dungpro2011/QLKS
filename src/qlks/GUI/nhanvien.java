    private void themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_themActionPerformed

        //Kiem tra cac textField
        if(tfMNV.getText().length()>0&&tfH.getText().length()>0&&tfT.getText().length()>0&&
            tfNS.getText().length()>0&&(rdNam.isSelected()||rdNu.isSelected())&&tfDC.getText().length()>0&&
            tfCMND.getText().length()>0&&tfSDT.getText().length()>0&&
            tfNVL.getText().length()>0&&tfL.getText().length()>0){
            //Tao moi nhan vien
            nv = new NhanVien();
            nv.setMaNhanVien(tfMNV.getText());
            nv.setHo(tfH.getText());
            nv.setTen(tfT.getText());
            if(nv.setNgaySinhKT(tfNS.getText()) == false){
                return;
            }
            if(rdNam.isSelected()){
                nv.setGioiTinh("Nam");
            }else{
                nv.setGioiTinh("Nữ");
            }
            nv.setDiaChi(tfDC.getText());
            nv.setCMMD(tfCMND.getText());
            nv.setDienThoai(tfSDT.getText());
            if(nv.setNgayVaoLamKT(tfNVL.getText()) == false){
                return;
            }
            if(nv.setLuongThang(tfL.getText()) == false){
                return;
            }
            nv.setMatKhau("123");
            nv.setXoa(false);

            //Them nhan vien thong qua BUS
            if(nvBUS.themNhanVien(nvDTO, nv)){
                model = new ModelNhanVien(nvDTO.docDanhSachNhanVien()).getModel();
                tbQLNV.setModel(model);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Vui lòng nhập đầy đủ thông tin");
        }
    }//GEN-LAST:event_themActionPerformed

